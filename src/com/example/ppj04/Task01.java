package com.example.ppj04;

public class Task01 {
    public static void main(String[] args) {
        int a = 78;
        int b = 47;

        double c = 0.234;
        double d = 6.7;

        char e = 'e';
        char f = 'f';

        System.out.println("Integer numbers");
        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(a / b);
        System.out.println(a % b);

        System.out.println();
        System.out.println("Real numbers");
        System.out.println(c + d);
        System.out.println(c - d);
        System.out.println(c * d);
        System.out.println(c / d);
        System.out.println(c % d);

        System.out.println();
        System.out.println("Character values");
        System.out.println(e + f);
        System.out.println(e - f);
        System.out.println(e * f);
        System.out.println(e / f);
        System.out.println(e % f);


    }
}
